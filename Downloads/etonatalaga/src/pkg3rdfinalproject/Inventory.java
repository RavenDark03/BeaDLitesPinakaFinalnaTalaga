     private void loadInventoryFromDatabase() {
        try (Connection conn = getConnection();
             PreparedStatement stmt = conn.prepareStatement("SELECT ingredient_name, stock, unit FROM inventory");
             ResultSet rs = stmt.executeQuery()) {
            tableModel.setRowCount(0); // Clear existing rows
            while (rs.next()) {
                String ingredient = rs.getString("ingredient_name");
                String stock = rs.getString("stock");
                String unit = rs.getString("unit");
                tableModel.addRow(new Object[]{ingredient, stock,unit});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading inventory: " + e.getMessage());
        }
    }

    private boolean isValidStockInput(String stockInput) {
    // Accepts: 100g, 100 g, 100.5g, 100ml, 100 ml, 100.5 ml, etc.
    return stockInput.matches("^\\d+(\\.\\d+)?\\s*(g|gram|grams|ml|ML)$");
}

// Helper method to prompt repeatedly until valid stock input is given
private String promptForValidStock() {
    while (true) {
        String input = JOptionPane.showInputDialog(
            null,
            "Stock (e.g., 100, 150.5):",
            "Enter Stock",
            JOptionPane.PLAIN_MESSAGE
        );
        if (input == null) return null; // User cancelled
        input = input.trim();
        if (input.matches("^\\d+(\\.\\d+)?$")) {
            return input;
        } else {
            JOptionPane.showMessageDialog(
                null,
                "Stock must be a number (optionally with decimals). Examples: 100, 250.5",
                "Invalid Stock",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }
}

// Helper for unit
private String promptForValidUnit() {
    String[] units = {"g", "gram", "grams", "ml", "ML"};
    while (true) {
        String input = (String) JOptionPane.showInputDialog(
            null,
            "Units:",
            "Enter Units",
            JOptionPane.PLAIN_MESSAGE,
            null,
            units,
            units[0]
        );
        if (input == null) return null;
        input = input.trim();
        if (input.matches("^(g|gram|grams|ml|ML)$")) {
            return input;
        } else {
            JOptionPane.showMessageDialog(
                null,
                "Unit must be one of: g, gram, grams, ml, ML",
                "Invalid Unit",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }
}

private void setupActionListeners() {
    addButton.addActionListener((ActionEvent e) -> {
    JTextField ingredientField = new JTextField();

    Object[] message = {
        "Ingredient Name:", ingredientField
    };

    int option = JOptionPane.showConfirmDialog(
        null,
        message,
        "Add Ingredient",
        JOptionPane.OK_CANCEL_OPTION
    );

    if (option == JOptionPane.OK_OPTION) {
        String ingredient = ingredientField.getText().trim();
        if (ingredient.isEmpty()) {
            JOptionPane.showMessageDialog(
                null,
                "Ingredient name is required!",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        String stockStr = promptForValidStock();
        if (stockStr == null) return; // User cancelled

        String unit = promptForValidUnit();
        if (unit == null) return; // User cancelled

        try (Connection conn = getConnection();
             PreparedStatement stmt = conn.prepareStatement(
                 "INSERT INTO inventory (ingredient_name, stock, unit) VALUES (?, ?, ?)")) {
            stmt.setString(1, ingredient);
            stmt.setString(2, stockStr);
            stmt.setString(3, unit);
            stmt.executeUpdate();
            loadInventoryFromDatabase(); // Refresh table
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(
                null,
                "Database error: " + ex.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }
});

    deleteButton.addActionListener((ActionEvent e) -> {
        int selectedRow = tableInventory.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(
                null,
                "Please select a row to delete!",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        } else {
            String ingredient = tableModel.getValueAt(selectedRow, 0).toString();
            try (Connection conn = getConnection();
                 PreparedStatement stmt = conn.prepareStatement(
                     "DELETE FROM inventory WHERE ingredient_name = ?")) {
                stmt.setString(1, ingredient);
                stmt.executeUpdate();
                loadInventoryFromDatabase(); // Refresh table
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(
                    null,
                    "Database error: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
                );
            }
        }
    });

    updateButton.addActionListener((ActionEvent e) -> {
    int selectedRow = tableInventory.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(
            null,
            "Please select a row to update!",
            "Error",
            JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    String currentIngredient = tableModel.getValueAt(selectedRow, 0).toString();

    JTextField ingredientField = new JTextField(currentIngredient);

    Object[] message = {
        "Ingredient Name:", ingredientField
    };

    int option = JOptionPane.showConfirmDialog(
        null,
        message,
        "Update Ingredient",
        JOptionPane.OK_CANCEL_OPTION
    );

    if (option == JOptionPane.OK_OPTION) {
        String newIngredient = ingredientField.getText().trim();
        if (newIngredient.isEmpty()) {
            JOptionPane.showMessageDialog(
                null,
                "Ingredient name is required!",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        String newStockStr = promptForValidStock();
        if (newStockStr == null) return; // User cancelled

        String newUnit = promptForValidUnit();
        if (newUnit == null) return; // User cancelled

        try (Connection conn = getConnection();
             PreparedStatement stmt = conn.prepareStatement(
                 "UPDATE inventory SET ingredient_name=?, stock=?, unit=? WHERE ingredient_name=?")) {
            stmt.setString(1, newIngredient);
            stmt.setString(2, newStockStr);
            stmt.setString(3, newUnit);
            stmt.setString(4, currentIngredient);
            stmt.executeUpdate();
            loadInventoryFromDatabase(); // Refresh table
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(
                null,
                "Database error: " + ex.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }
});
}
    
    
    
    
    
